# 파이썬 빠른 시작
# Python은 해석형 프로그래밍 언어입니다. 즉, 개발자는 텍스트 편집기에서 Python(.py) 파일을 작성한 다음 해당 파일을 Python 인터프리터에 넣어 실행합니다.

# 첫 번째 Python 파일을 작성해 보겠습니다. hello.py이 파일은 모든 텍스트 편집기에서 작성할 수 있습니다.

# hello.py:
print("Hello, World!")
# 간단합니다. 파일을 저장하세요. 명령줄을 열고 파일을 저장한 디렉터리로 이동한 후 다음을 실행하세요.

# C:\Users\Your Name>python hello.py
# 출력은 다음과 같습니다.

# Hello, World!
# 축하합니다. 첫 번째 Python 프로그램을 작성하고 실행했습니다.