# 문자열 메서드
#######################################
# Python에는 문자열에 사용할 수 있는 내장 메서드 세트가 있습니다.

# 참고: 모든 문자열 메서드는 새 값을 반환합니다. 원본 문자열은 변경되지 않습니다.

# Method	Description
# 문자열 변환 관련
# capitalize()	문자열의 첫 글자를 대문자로, 나머지는 소문자로 변환
# casefold()	문자열을 매우 강력한 소문자 처리 (locale 상관없이 통일된 lower)
# lower()	    문자열 전체를 소문자로 변환
# upper()	    문자열 전체를 대문자로 변환
# swapcase()	대문 ↔ 소문자 서로 바꾸기
# title()	    각 단어의 첫 글자만 대문자로 변환 (제목 스타일)

# 문자열 검사(조건 확인)
# isalnum()	    문자들이 전부 영문자 + 숫자면 True
# isalpha()	    문자들이 전부 영문자면 True
# isascii()	    ASCII 문자(0~127)만 포함하면 True
# isdecimal()	10진수 숫자만 포함하면 True (0~9)
# isdigit()	    숫자로 인식되면 True (유니코드 숫자 포함)
# isnumeric()	더 넓은 범위의 숫자(분수, 원 문자 등)까지 숫자로 인정
# isidentifier()	숫자로 인식되면 True (유니코드 숫자 포함)
# islower()	    모든 문자가 소문자면 True
# isupper()	    모든 문자가 대문자면 True
# isprintable()	출력 가능한 문자만 포함하면 True
# isspace()	    공백 문자(스페이스, 탭 등)만 포함하면 True
# istitle()	    제목 규칙(첫 문자만 대문자)을 따르고 있으면 True

# 문자열 검색
# find()	    지정한 문자열을 앞에서부터 검색 → 위치 반환 (못 찾으면 -1)
# index()	    find()와 같지만, 못 찾으면 에러 발생
# rfind()   	지정한 문자열을 뒤에서부터 검색
# rindex()	    rfind()와 같지만, 못 찾으면 에러
# count()	    부분 문자열이 몇 번 나오는지 세어 줌

# 문자열 나누기 / 붙이기
# split()	    구분자를 기준으로 문자열을 리스트로 분리
# rsplit()	    split()과 같지만 오른쪽부터 분리
# splitlines()	줄바꿈을 기준으로 분리
# partition()	문자열을 **3부분(tuple)**으로 나눔 — (앞, 구분자, 뒤)
# rpartition()	partition의 오른쪽 버전
# join()	    리스트/튜플 등의 요소 사이에 문자열을 넣어 합침

# 문자열 정렬 및 공백 제거 (trim)
# center()	    문자열을 지정 폭 안에서 가운데 정렬
# ljust()	    좌측 정렬
# rjust()	    우측 정렬
# strip()	    양쪽 공백 제거
# lstrip()	    왼쪽 공백 제거
# rstrip()	    오른쪽 공백 제거
# zfill()	    왼쪽에 0을 채워 문자열 길이를 맞춤 ("007" 같은 형태)

# 문자열 대체/변환
# replace()	    특정 문자열을 새 문자열로 교체
# translate()	변환 테이블(maketrans)를 이용해 문자 치환
# maketrans()	translate에서 사용할 변환 테이블 생성

# 문자열 검사 (시작/끝)
# startswith()	문자열이 특정 문자열로 시작하면 True
# endswith()	문자열이 특정 문자열로 끝나면 True

# 기타
# encode()	    문자열을 인코딩(UTF-8 등)된 바이트 객체로 변환
# expandtabs()	\t 탭을 지정한 크기의 공백으로 변환
# format()	    문자열 포맷팅 ("{} {}".format(a, b))
# format_map()	딕셔너리 기반 포맷팅