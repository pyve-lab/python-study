#######################################
# 항목 설정 - 데이터 유형
#######################################
# 설정 항목은 모든 데이터 유형이 될 수 있습니다.

# 예
# 문자열, 정수 및 부울 데이터 유형:
set1 = {"apple", "banana", "cherry"}
