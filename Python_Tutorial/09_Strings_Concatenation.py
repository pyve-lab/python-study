#######################################
# 문자열 연결
#######################################
# 두 개의 문자열을 연결하거나 결합하려면 + 연산자를 사용하면 됩니다.

# 예
# 변수를 a변수와 b변수로 병합합니다 c.
a = "Hello"
b = "World"
c = a + b
print(c)
# HelloWorld

# 예
# 그 사이에 공백을 추가하려면 :를 추가합니다 " ".
a = "Hello"
b = "World"
c = a + " " + b
print(c)
# Hello World

