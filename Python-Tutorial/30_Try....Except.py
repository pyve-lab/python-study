"""
============================================================
📌 Python 예외 처리 (try / except)
============================================================

● try 블록 : 오류가 발생할 수 있는 코드를 작성
● except 블록 : 오류 발생 시 실행할 코드 작성
● else 블록 : 오류가 없을 때 실행할 코드
● finally 블록 : 오류와 관계없이 항상 실행하는 코드

예:
    try:
        print(x)
    except:
        print("An exception occurred")
"""

"""
============================================================
📌 1. try / except 기본 구조
============================================================

예:
    try:
        print(x)      # x가 정의되지 않음
    except:
        print("에러 발생")

# try에서 오류 발생 → except 실행
"""
# 파이썬 기본 예외 처리 예제

try:
    print(x)  # x가 정의되지 않음
except:
    print("에러 발생")
# 결과 : 에러 발생

"""
============================================================
📌 2. 여러 개의 except 블록 (오류 타입 구분)
============================================================

예:
    try:
        print(x)
    except NameError:
        print("x는 정의되지 않음")
    except:
        print("다른 오류 발생")
"""
# 파이썬 여러 except 블록 예제

try:
    print(x)  # x가 정의되지 않음
except NameError:
    print("x는 정의되지 않음")
except:
    print("다른 오류 발생")
# 결과 : x는 정의되지 않음

"""
============================================================
📌 3. else 블록 — 오류가 없을 때 실행
============================================================

예:
    try:
        print("Hello")
    except:
        print("문제 발생")
    else:
        print("오류 없음 → else 실행")
"""
# 파이썬 else 블록 예제

try:
    print("Hello")
except:
    print("문제 발생")
else:
    print("오류 없음 → else 실행")
# 결과 : 
# Hello
# 오류 없음 → else 실행

"""
============================================================
📌 4. finally 블록 — 항상 실행됨
============================================================

예:
    try:
        print(x)
    except:
        print("문제 발생")
    finally:
        print("try-except 종료됨")

● 파일 닫기 / 리소스 정리 시 유용

예:
    try:
        f = open("demofile.txt")
        try:
            f.write("Hello")
        except:
            print("쓰기 실패")
        finally:
            f.close()      # 항상 실행 (파일 닫기)
    except:
        print("파일 열기 실패")
"""
# 파이썬 finally 블록 예제

try:
    print(x)  # x가 정의되지 않음
except:
    print("문제 발생")
finally:
    print("try-except 종료됨")
# 결과 :
# 문제 발생
# try-except 종료됨

"""
============================================================
📌 5. 예외 강제 발생 (raise)
============================================================

● 개발자가 직접 오류를 발생시킬 수 있음
● 비정상 입력 또는 조건 처리가 필요할 때 사용

예:
    x = -1
    if x < 0:
        raise Exception("0보다 작은 숫자는 허용되지 않음")

특정 오류 타입 지정 가능:
    x = "hello"
    if not isinstance(x, int):
        raise TypeError("정수만 허용됩니다")
"""
# 파이썬 예외 강제 발생 예제

x = -1
try:
    if x < 0:
        raise Exception("0보다 작은 숫자는 허용되지 않음")
except Exception as e:
    print(e)
# 결과 : 0보다 작은 숫자는 허용되지 않음

"""
============================================================
📌 핵심 요약
============================================================

✔ try → 문제 발생 가능 코드  
✔ except → 오류 처리  
✔ else → 오류 없을 때 실행  
✔ finally → 항상 실행 (리소스 정리)  
✔ raise → 강제로 오류 발생  

============================================================
"""
