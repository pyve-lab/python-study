"""
============================================================
📌 Python 배열(Array) — 실제로는 리스트(List)를 사용
============================================================

💡 중요!
Python에는 기본적인 “배열 자료형"이 없음.
대신 동일한 기능을 대부분 "리스트(list)"로 수행함.
진짜 배열이 필요하면 NumPy 같은 외부 라이브러리가 필요.

예:
    cars = ["Ford", "Volvo", "BMW"]
"""

"""
============================================================
📌 1. 배열(리스트)이란?
============================================================
- 여러 값을 하나의 변수에 저장할 수 있는 자료 구조
- 인덱스를 통해 요소에 접근
- 반복, 추가, 삭제 등 다양한 조작 가능

예를 들어:
    car1 = "Ford"
    car2 = "Volvo"
    car3 = "BMW"

→ 이렇게 저장하면 비효율적  
→ 리스트(배열)를 사용하면 해결됨:
    cars = ["Ford", "Volvo", "BMW"]
"""
# 파이썬 배열(리스트) 개념 정리

cars = ["Ford", "Volvo", "BMW"]
print(cars)
# 결과 : ['Ford', 'Volvo', 'BMW']

"""
============================================================
📌 2. 배열 요소 접근 (Indexing)
============================================================

첫 번째 요소 접근:
    cars[0]

값 변경:
    cars[0] = "Toyota"
"""
# 파이썬 배열(리스트) 요소 접근 및 변경

cars = ["Ford", "Volvo", "BMW"]
print(cars[0])  # Ford
cars[0] = "Toyota"
print(cars)      # ['Toyota', 'Volvo', 'BMW']

"""
============================================================
📌 3. 배열 길이 확인 (len)
============================================================
len() → 요소 개수 반환

    x = len(cars)

참고:
- 인덱스는 0부터 시작하므로
  길이 = 최대 인덱스 + 1
"""
# 파이썬 배열(리스트) 길이 확인 예제

cars = ["Ford", "Volvo", "BMW"]
print(len(cars))  # 3

"""
============================================================
📌 4. 배열 반복 (for loop)
============================================================
for x in cars:
    print(x)
"""
# 파이썬 배열(리스트) 반복 예제

cars = ["Ford", "Volvo", "BMW"]
for x in cars:
    print(x)
# 결과 : Ford Volvo BMW

"""
============================================================
📌 5. 배열 요소 추가 (append)
============================================================
맨 뒤에 값 추가:

    cars.append("Honda")
"""
# 파이썬 배열(리스트) 요소 추가 예제

cars = ["Ford", "Volvo", "BMW"]
cars.append("Honda")
print(cars)  # ['Ford', 'Volvo', 'BMW', 'Honda']

"""
============================================================
📌 6. 배열 요소 제거
============================================================

1) 위치로 삭제 (pop)
    cars.pop(1)   # index 1 요소 삭제

2) 값으로 삭제 (remove)
    cars.remove("Volvo")

⚠ remove()는 해당 값의 "첫 번째 항목만" 삭제함
"""
# 파이썬 배열(리스트) 요소 제거 예제

# 위치로 삭제 (pop)
cars = ["Ford", "Volvo", "BMW"]
cars.pop(1)
print(cars)  # ['Ford', 'BMW']

# 값으로 삭제 (remove)
cars = ["Ford", "Volvo", "BMW"]
cars.remove("Volvo")
print(cars)  # ['Ford', 'BMW']

"""
============================================================
📌 7. 리스트(배열) 메서드 요약
============================================================

append()      → 뒤에 요소 추가
clear()       → 모든 요소 제거
copy()        → 리스트 복사
count()       → 특정 값이 몇 개 있는지 반환
extend()      → 다른 리스트(또는 iterable)의 요소를 뒤에 추가
index()       → 특정 값의 첫 번째 인덱스 반환
insert()      → 특정 위치에 요소 삽입
pop()         → 특정 위치의 요소 제거
remove()      → 특정 값을 가진 첫 번째 요소 제거
reverse()     → 요소 순서 뒤집기
sort()        → 요소를 정렬
"""

