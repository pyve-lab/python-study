"""
📌 파이썬 주석 (Comments)
==========================

주석이란?
-----------
주석은 Python 코드에 설명을 추가하거나,
코드를 더 읽기 쉽게 만들기 위해 사용됩니다.

또한 테스트 중에 특정 코드를 실행하지 않도록 막는 데에도 사용됩니다.
"""

"""
📌 주석 작성 방법
-----------------
주석은 # 기호로 시작하며,
Python은 # 뒤의 모든 내용을 무시합니다.

예:
    # This is a comment
    print("Hello, World!")
"""
# 파이썬 주석

# This is a comment
print("Hello, World!")


"""
📌 줄 끝에 주석 작성
-------------------
주석은 코드 뒤쪽에도 작성할 수 있으며,
Python은 해당 줄의 나머지 부분을 모두 무시합니다.

예:
    print("Hello, World!")  # This is a comment
"""
# 파이썬 줄 끝 주석

print("Hello, World!") # This is a comment

"""
📌 주석으로 실행 방지
---------------------
주석은 반드시 설명용 텍스트일 필요는 없으며,
코드를 임시로 비활성화할 때도 사용됩니다.

예:
    # print("Hello, World!")
    print("Cheers, Mate!")
"""
# 파이썬 주석으로 실행 방지

# print("Hello, World!")
print("Cheers, Mate!")
# 결과 : Cheers, Mate!