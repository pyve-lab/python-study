"""
📌 변수 유형 지정 (Type Casting)
===============================

Python에서는 필요할 때 변수의 데이터 타입을 직접 지정할 수 있습니다.
이를 **캐스팅(casting)** 이라고 합니다.

Python은 객체 지향 언어이며,
정수(int), 부동소수(float), 문자열(str) 같은 기본 타입도 모두 클래스(class)로 구성됩니다.

따라서 파이썬에서의 타입 변환은 **생성자 함수(Constructor)** 를 사용하여 수행됩니다.


기본 캐스팅 함수
-----------------
int()   → 정수형(Integer)으로 변환  
float() → 실수형(Float)으로 변환  
str()   → 문자열(String)으로 변환


📌 int() — 정수로 변환
--------------------------
정수 리터럴, 소수점이 있는 숫자(소수점 제거),  
또는 정수를 나타내는 문자열을 정수로 변환합니다.

예:
    x = int(1)      # 1
    y = int(2.8)    # 2 (소수점 제거)
    z = int("3")    # 3


📌 float() — 실수로 변환
---------------------------
정수, 실수, 또는 실수 형태의 문자열을 float 으로 변환합니다.

예:
    x = float(1)       # 1.0
    y = float(2.8)     # 2.8
    z = float("3")     # 3.0
    w = float("4.2")   # 4.2


📌 str() — 문자열로 변환
--------------------------
숫자도 문자열 형태로 변환할 수 있습니다.

예:
    x = str("s1")   # 's1'
    y = str(2)      # '2'
    z = str(3.0)    # '3.0'
"""
# 파이썬 캐스팅

x = int(1)      # 1
y = int(2.8)    # 2
z = int("3")    # 3

