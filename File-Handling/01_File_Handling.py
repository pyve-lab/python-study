"""
==========================================================
📌 파이썬 파일 처리 (File Handling)
==========================================================

파일 처리는 모든 웹·데스크톱·서버 애플리케이션에서 매우 중요한 기능이다.

파이썬에서는 파일 생성·읽기·쓰기·추가·삭제 등을 수행할 수 있으며,
가장 핵심이 되는 함수는 open() 함수이다.
"""

"""
==========================================================
📌 1. open() 함수 — 파일 열기
==========================================================

open() 함수는 두 개의 매개변수를 받는다:

    1) 파일 이름 (예: "data.txt")
    2) 모드(mode) — 파일을 어떻게 열 것인지

예:
    f = open("demofile.txt", "r")
"""
# 파이썬 open() 함수 예시

f = open("c:\\Users\\권나현\\Desktop\\python-study\\file_test\\demofile.txt", "r")  # 읽기 모드로 파일 열기
print(f)  # <_io.TextIOWrapper name='c:\\Users\\권나현\\Desktop\\python-study\\file_test\\demofile.txt' mode='r' encoding='cp949'>

"""
==========================================================
📌 2. 파일 열기 모드 종류
==========================================================

✔ "r" → 읽기(Read)
    - 기본값
    - 파일이 존재해야 함 (없으면 오류 발생)

✔ "a" → 추가(Append)
    - 기존 내용 뒤에 추가
    - 파일이 없으면 새로 생성

✔ "w" → 쓰기(Write)
    - 기존 파일 내용을 모두 덮어씀
    - 파일이 없으면 새로 생성

✔ "x" → 생성(Create)
    - 새로운 파일 생성
    - 이미 존재하면 오류 발생
"""
# 파이썬 파일 열기 모드 종류 예시

f_read = open("demofile.txt", "r")   # 읽기 모드
f_append = open("demofile.txt", "a")  # 추가 모드
f_write = open("demofile.txt", "w")   # 쓰기 모드
# f_create = open("demofile.txt", "x")  # 생성 모드 (파일이 이미 있으면 오류 발생)

"""
==========================================================
📌 3. 텍스트 모드 / 바이너리 모드
==========================================================

✔ "t" → 텍스트 모드 (기본값)
✔ "b" → 바이너리 모드 (예: 이미지, 영상, PDF 등 다룰 때 사용)

조합하여 사용 가능:
    "rb" → 바이너리 읽기
    "wb" → 바이너리 쓰기
    "ab" → 바이너리 추가
"""
# 파이썬 텍스트 모드 / 바이너리 모드 예시

f_text = open("demofile.txt", "rt")   # 텍스트 읽기 모드
f_binary = open("demofile.bin", "rb")  # 바이너리 읽기 모드

"""
==========================================================
📌 4. 기본 문법
==========================================================

파일 읽기 모드로 열 때:

f = open("demofile.txt")  
# 기본값 "rt" 와 동일 (r = read, t = text)


즉 아래 두 코드는 동일함:

f = open("demofile.txt")
f = open("demofile.txt", "rt")
"""
# 파이썬 파일 열기 기본 문법 예시

f = open("c:\\Users\\권나현\\Desktop\\python-study\\file_test\\demofile.txt")  # 읽기 모드로 파일 열기
print(f)  # <_io.TextIOWrapper name='c:\\Users\\권나현\\Desktop\\python-study\\file_test\\demofile.txt' mode='r' encoding='cp949'>

"""
==========================================================
📌 5. 중요한 주의사항
==========================================================

- 읽기("r") 모드로 열 때 파일이 존재하지 않으면 오류 발생
- 쓰기("w"), 추가("a")는 파일이 없어도 자동 생성됨
- 생성("x")는 파일이 있으면 오류 발생
"""
# 파이썬 파일 열기 주의사항 예시

# f = open("nonexistent.txt", "r")  # 오류 발생: FileNotFoundError
f = open("newfile.txt", "w")       # 새 파일 생성 (없으면)
print(f)  # <_io.TextIOWrapper name='newfile.txt' mode='w' encoding='cp949'>

"""
==========================================================
📌 정리
----------------------------------------------------------
- open() 함수로 파일을 열고, 파일 모드에 따라 동작이 달라진다.
- 텍스트/바이너리 모드를 조합하여 사용할 수 있다.
- "r" 모드는 파일이 반드시 존재해야 한다.
- 파일 처리는 서버/웹 애플리케이션에서 매우 중요한 기술이다.

"""
