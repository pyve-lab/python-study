"""
📌 OOP (Object-Oriented Programming, 객체 지향 프로그래밍)
==========================================================

OOP는 프로그램을 '객체' 단위로 구성하여 구조를 명확하게 하고, 재사용성과 유지보수를 향상시키는 프로그래밍 방식입니다.

파이썬은 대표적인 객체 지향 언어로, 클래스(Class)와 객체(Object)를 사용해 코드를 조직화할 수 있습니다.


📌 OOP의 주요 장점
--------------------
1) 프로그램 구조가 명확해진다  
2) 유지보수 및 디버깅이 쉬워진다  
3) 재사용성이 높아진다  
4) DRY 원칙(Don't Repeat Yourself)을 지키기 쉽다  
   → 같은 코드를 반복 작성하지 않고, 함수나 클래스로 분리하여 재사용


==========================================================
📌 클래스(Class)와 객체(Object)
==========================================================

✔ 클래스(Class)
    - 객체를 만들기 위한 설계도(blueprint)
    - 속성(변수)과 메서드(함수)를 정의

✔ 객체(Object)
    - 클래스를 기반으로 생성된 실제 사용 가능한 실체(instance)
    - 클래스에 정의된 변수·함수를 그대로 가진다

예시 관계:
    Class     →     Objects
    --------------------------
    Fruit     →     Apple, Banana, Mango
    Car       →     Volvo, Audi, Toyota


==========================================================
📌 클래스로부터 객체를 생성하면?
----------------------------------------------------------
- 해당 클래스 내부에 정의된 모든 속성과 메서드를 "상속"받는다.
- 즉, 객체는 클래스의 기능을 그대로 사용할 수 있다.


==========================================================
📌 OOP가 다루는 주요 개념들 (이후 학습 예정)
==========================================================

1) 클래스(Class)와 객체(Object)
2) __init__() 생성자 메서드
3) self 매개변수
4) 속성(Attributes)과 메서드(Methods)
5) 상속(Inheritance)
6) 다형성(Polymorphism)
7) 캡슐화(Encapsulation)
8) 내부 클래스(Inner Class)

"""
