"""
==========================================================
📌 파이썬 클래스(Class)와 객체(Object)
==========================================================

파이썬은 객체 지향 프로그래밍(OOP)을 지원하는 언어이며,
Python의 거의 모든 것은 '객체'로 구성됩니다.

✔ 클래스(Class)
    - 객체를 생성하기 위한 설계도(blueprint)
    - 속성(변수)과 메서드(함수)를 정의하는 틀

✔ 객체(Object)
    - 클래스를 기반으로 생성된 실체(인스턴스)
    - 클래스에 정의된 속성과 메서드를 그대로 가진다


==========================================================
📌 1. 클래스 생성하기 (class 키워드)
==========================================================

class MyClass:
    x = 5

→ MyClass라는 클래스를 정의하고, x라는 속성을 가진다.
"""
# 파이썬 클래스와 객체

class MyClass:
    x = 5

"""
==========================================================
📌 2. 객체 생성하기
==========================================================

p1 = MyClass()
print(p1.x)

→ MyClass로부터 p1이라는 객체를 만들고,
   객체 내부의 속성 x 값을 출력한다.
"""
# 파이썬 객체 생성

p1 = MyClass()
print(p1.x)
# 결과 : 5

"""
==========================================================
📌 3. 객체 삭제하기 (del 키워드)
==========================================================

del p1

→ 생성된 객체 p1을 삭제한다.
"""
# 파이썬 객체 삭제

del p1
print(p1)
# 결과 : NameError: name 'p1' is not defined

"""
==========================================================
📌 4. 여러 객체 생성하기
==========================================================

p1 = MyClass()
p2 = MyClass()
p3 = MyClass()

print(p1.x)
print(p2.x)
print(p3.x)

✔ 각 객체는 서로 독립적이며, 클래스 속성의 사본을 가진다.
"""
# 파이썬 여러 객체 생성

p1 = MyClass()
p2 = MyClass()
p3 = MyClass()
print(p1.x)  # 결과 : 5
print(p2.x)  # 결과 : 5
print(p3.x)  # 결과 : 5

"""
==========================================================
📌 5. pass 문 — 비어 있는 클래스 정의
==========================================================

클래스 본문을 비워둘 수는 없기 때문에
내용이 없을 경우 오류를 방지하기 위해 pass 문을 사용한다.

예:

class Person:
    pass

→ 나중에 내용을 채울 클래스를 임시로 만들어둘 때 유용하다.

"""
# 파이썬 비어 있는 클래스 정의

class Person:
    pass
# Person 클래스는 현재 비어 있지만, 오류 없이 정의됨
