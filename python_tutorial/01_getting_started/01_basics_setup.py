# Python Intro
# 1. 파이썬이란 ?
# 1991년 **귀도 반 로섬(Guido van Rossum)**에 의해 개발된 인터프리터 방식의 프로그래밍 언어입니다. 
# 배우기 쉽고 강력하여 현재 가장 인기 있는 언어 중 하나입니다.


# 2. 주요 용도
# 파이썬은 범용 언어로, 다양한 분야에서 활용됩니다.
# 웹 개발: 서버 사이드 웹 애플리케이션 구축 (Django, Flask 등)
# 데이터 사이언스: 빅데이터 처리, 복잡한 수학 연산 및 분석
# 자동화 & 스크립팅: 반복 업무 자동화 및 시스템 유틸리티 제작
# 데이터베이스: DB 시스템 연결 및 파일 읽기/수정/쓰기
# 소프트웨어 개발: 프로토타입 제작부터 배포용 소프트웨어까지 전천후 활용


# 3. 파이썬을 사용하는 이유 (왜 파이썬인가? (Why Python?)
# 플랫폼 독립적: Windows, Mac, Linux, Raspberry Pi 등 다양한 OS에서 실행됩니다.
# 높은 가독성: 영어와 유사한 문법 구조로 읽고 쓰기 쉽습니다.
# 높은 생산성: 다른 언어에 비해 훨씬 적은 줄의 코드로 같은 기능을 구현할 수 있습니다.
# 인터프리터 언어: 코드를 작성하면 컴파일 과정 없이 즉시 실행되어 빠른 피드백이 가능합니다.
# 멀티 패러다임: 절차 지향, 객체 지향, 함수형 프로그래밍 방식을 모두 지원합니다.


# 4. 파이썬의 문법적 특징 (Syntax)
# 구문 종료시 줄바꿈(Enter)로 명령을 구분합니다. (자바나 C언어는 세미콜론(;) 사용)
# 범위를 지정할때 들여쓰기와 공백을 사용합니다. (자바나 C언어는 중괄호({}) 사용)
# 수학적이로 영어와 유사하여 직관적입니다. (자바나 C언어는 상대적으로 복잡함)