# Concatenate Strings
# 문자열 연결 
# 1. 문자열 연결 (String Concatenation)
# 두 개의 문자열을 연결(concatenate), 즉 결합하기 위해 + 연산자를 사용할 수 있습니다.

# 예시 : 변수 $\text{a}$와 변수 $\text{b}$를 변수 $\text{c}$로 병합합니다.
'''
a = "Hello"
b = "World"
c = a + b
print(c)
# 출력: HelloWorld
'''

# 예시 2: 두 문자열 사이에 공백을 추가하려면, " "을 함께 더해줍니다.

'''
a = "Hello"
b = "World"
c = a + " " + b
print(c)
# 출력: Hello World
'''