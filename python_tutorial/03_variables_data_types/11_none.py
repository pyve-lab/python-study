# None
# 1. 파이썬 None(값이 없음)
# None은 파이썬에서 값이 존재하지 않음을 나타내는 특별한 상수입니다.
# 용도: 변수에 명시적으로 "값이 설정되지 않음" 또는 "비어 있음"을 표시하기 위해 할당됩니다.
# 자료형: None의 자료형은 **NoneType**이며, None은 이 NoneType 객체의 유일한 인스턴스입니다.

x = None
print(type(x))  # 출력: <class 'NoneType'>


# 2. None 사용 예시
# 변수 x에 None을 할당하고 출력하면, 값이 없다는 의미의 None이 그대로 출력됩니다.

x = None
print(x)  # 출력: None


# 3. None 값의 비교 및 특징
# 다른 값과 달리, None 값과 비교할 때는 동일성 연산자(is 또는 is not)를 사용하는 것이 파이썬의 표준이자 권장 사항입니다. 
# 동등성 연산자(== 또는 !=)도 작동은 하지만, is를 사용하는 것이 더 명확하고 안전합니다.
'''
is None	: 값이 None인지 확인, 변수가 None과 완전히 동일한 객체인가?"를 확인합니다.

is not None	: 값이 None이 아닌지 확인, 값이 설정되어 있거나 준비되었는지 확인하는 데 자주 사용됩니다.

'''

result = None

# result가 None인지 확인 (권장되는 방식)
if result is None:
  print("No result yet") # 출력: No result yet
  
  
# 4. 불리언 평가: None은 False
# None은 불리언 문맥에서 평가될 때 항상 False로 간주됩니다.
# None의 불리언 값 확인
print(bool(None)) # 출력: False


# 5. 함수 반환값으로서의 None
# 함수가 명시적으로 return 문을 사용해 값을 반환하지 않으면, 파이썬은 기본적으로 None을 반환합니다
# 이 함수는 명시적인 return 문이 없음
def myfunc():
  x = 5

x = myfunc()    # 함수가 끝난 후 None을 x에 할당
print(x)        # 출력: None
# 따라서 함수가 의도적으로 값을 돌려주지 않을 때, 그 함수의 결과값은 자동으로 None이 됩니다.


# 6. None은 0이나 빈 문자열이 아니다
# 초보자들이 흔히 오해하는 것 중 하나는 None을 숫자 0이나 빈 문자열 ""과 같다고 생각하는 것입니다. 이는 완전히 다릅니다.
'''
None: 값이 아예 존재하지 않음을 의미합니다. (값이 설정되지 않은 상태)

0: 숫자 **0**이라는 실제 값이 존재합니다.

"": 길이가 0인 문자열이라는 실제 객체가 존재합니다.

'''

x = None

print(x == 0)    # 출력: False (None과 0은 다르다)
print(x == "")   # 출력: False (None과 빈 문자열은 다르다)
print(x is 0)    # 출력: False (객체 자체도 다르다)


# 7. None은 변경 불가능한 값 (Immutable)
# None이 변경 불가능하다는 것은 메모리에 딱 한 번 만들어진 후에는 그 상태나 내용을 바꿀 수 없다는 의미입니다.
# None은 애초에 '값이 없음'이라는 상태 자체를 나타냅니다. 따라서 변경할 내용 자체가 없습니다.
# 이 특성 덕분에 파이썬은 프로그램 전체를 통틀어 이 None 객체를 단 하나만 메모리에 만들어 두고 사용합니다.