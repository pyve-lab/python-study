# Bitwise Operators

# 비트wise 연산자는 숫자(정수)를 2진수(binary)로 비교하고 연산하는 데 사용됩니다.
# 이 연산은 피연산자의 개별 비트(bit) 수준에서 작동합니다.

# ------------------------------------------------------------------------------
# 비트wise 연산자 표 (Bitwise Operators List)
# ------------------------------------------------------------------------------
#
# 연산자 | 이름 (Name)             | 설명 (Description)
# :---: | :---:                   | :---:
# &     | AND (논리곱)            | 두 비트가 모두 1일 때만 1로 설정됩니다.
# |     | OR (논리합)             | 두 비트 중 하나라도 1이면 1로 설정됩니다.
# ^     | XOR (배타적 논리합)     | 두 비트 중 하나만 1일 때 1로 설정됩니다. (같으면 0)
# ~     | NOT (보수)              | 모든 비트를 반전시킵니다. (0은 1로, 1은 0으로)
# <<    | Zero fill left shift (왼쪽 시프트) | 오른쪽에서 0을 밀어 넣으며 왼쪽으로 비트를 이동시킵니다.
# >>    | Signed right shift (오른쪽 시프트) | 왼쪽에서 부호 비트(signed bit) 복사본을 밀어 넣으며 오른쪽으로 비트를 이동시킵니다.

# ------------------------------------------------------------------------------
# 예시 1: & 연산자 (AND)
# ------------------------------------------------------------------------------
# & 연산자는 각 비트를 비교하여, 두 비트가 모두 1일 때만 1로 설정하고, 그렇지 않으면 0으로 설정합니다.

# 6 (10진수) = 0110 (2진수)
# 3 (10진수) = 0011 (2진수)
# --------------------
# 결과:       0010 (2진수) -> 2 (10진수)

print(6 & 3)
# 출력: 2

# ------------------------------------------------------------------------------
# 예시 2: | 연산자 (OR)
# ------------------------------------------------------------------------------
# | 연산자는 각 비트를 비교하여, 두 비트 중 하나라도 1이면 1로 설정하고, 그렇지 않으면 0으로 설정합니다.

# 6 (10진수) = 0110 (2진수)
# 3 (10진수) = 0011 (2진수)
# --------------------
# 결과:       0111 (2진수) -> 7 (10진수)

print(6 | 3)
# 출력: 7

# ------------------------------------------------------------------------------
# 예시 3: ^ 연산자 (XOR)
# ------------------------------------------------------------------------------
# ^ 연산자는 각 비트를 비교하여, 두 비트 중 하나만 1일 때 1로 설정하고, (두 비트가 모두 1이거나 모두 0일 경우) 0으로 설정합니다.

# 6 (10진수) = 0110 (2진수)
# 3 (10진수) = 0011 (2진수)
# --------------------
# 결과:       0101 (2진수) -> 5 (10진수)

print(6 ^ 3)
# 출력: 5