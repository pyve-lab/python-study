# Identity Operators

# 식별 연산자는 두 값이 같은지(equal)가 아니라, 실제로 메모리 상의 '같은 객체(same object)'를 가리키고 있는지를 비교하는 데 사용됩니다.

# ------------------------------------------------------------------------------
# 식별 연산자 표 (Identity Operators List)
# ------------------------------------------------------------------------------
# 연산자 | 설명 (Description)                                     | 예시 (Example)
# :---: | :---:                                                  | :---:
# is    | 두 변수가 같은 객체(동일한 메모리 위치)를 가리키면 True 반환 | x is y
# is not| 두 변수가 같은 객체를 가리키고 있지 않으면 True 반환     | x is not y

# ------------------------------------------------------------------------------
# 예시 1: is 연산자 사용
# ------------------------------------------------------------------------------
# is 연산자는 두 변수가 같은 객체를 가리키면 True를 반환합니다.

x = ["apple", "banana"]
y = ["apple", "banana"]
z = x # z는 x가 가리키는 객체(메모리 위치)를 그대로 참조합니다.

print(x is z)  # True (x와 z는 같은 메모리 객체를 참조)
print(x is y)  # False (x와 y는 값이 같지만, 메모리 상의 별개의 리스트 객체임)
print(x == y)  # True (x와 y는 값이 같음)


# ------------------------------------------------------------------------------
# 예시 2: is not 연산자 사용
# ------------------------------------------------------------------------------
# is not 연산자는 두 변수가 같은 객체를 가리키고 있지 않으면 True를 반환합니다.

x = ["apple", "banana"]
y = ["apple", "banana"]

print(x is not y) # True (x와 y는 별개의 객체이므로 같지 않다)


# ------------------------------------------------------------------------------
# is 연산자와 == 연산자의 차이점
# ------------------------------------------------------------------------------
#
# is  - 두 변수가 메모리 상의 같은 객체를 가리키는지 확인합니다. (객체의 동일성/Identity)
# ==  - 두 변수의 '값'이 같은지 확인합니다. (값의 동등성/Equality)

# 예시:
x_diff = [1, 2, 3]
y_diff = [1, 2, 3]

print(f"\n--- is 와 == 비교 ---")
print(f"x_diff == y_diff (값 동등성): {x_diff == y_diff}") # True (값이 같음)
print(f"x_diff is y_diff (객체 동일성): {x_diff is y_diff}") # False (별개의 리스트 객체임)