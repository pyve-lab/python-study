# Comparison Operators

# 비교 연산자는 두 값(value)을 비교하는 데 사용됩니다.
# 비교 연산의 결과는 항상 True 또는 False(불리언 값)로 반환됩니다.

# ------------------------------------------------------------------------------
# 비교 연산자 표 (Comparison Operators List)
# ------------------------------------------------------------------------------
# 연산자 | 이름 (Name)             | 설명 (Description)             | 예시 (Example)
# :---: | :---:                   | :---:                          | :---:
# ==    | 같다 (Equal)            | 두 값이 같으면 True            | x == y
# !=    | 같지 않다 (Not equal)   | 두 값이 같지 않으면 True       | x != y
# >     | 크다 (Greater than)     | 왼쪽 값이 크면 True            | x > y
# <     | 작다 (Less than)        | 왼쪽 값이 작으면 True          | x < y
# >=    | 크거나 같다 (Greater than or equal to)| 왼쪽 값이 크거나 같으면 True | x >= y
# <=    | 작거나 같다 (Less than or equal to) | 왼쪽 값이 작거나 같으면 True  | x <= y


# ------------------------------------------------------------------------------
# 예시: 비교 연산자 사용 (결과는 True 또는 False)
# ------------------------------------------------------------------------------
x = 5
y = 3

# print(f"x = {x}, y = {y}")

# 같다 (Equal: ==)
print(x == y)

# 같지 않다 (Not equal: !=)
print(x != y)

# 크다 (Greater than: >)
print(x > y)

# 작다 (Less than: <)
print(x < y)

# 크거나 같다 (Greater than or equal to: >=)
print(x >= y)

# 작거나 같다 (Less than or equal to: <=)
print(x <= y)


# ------------------------------------------------------------------------------
# 비교 연산자 체인 (Chaining Comparison Operators)
# ------------------------------------------------------------------------------

# 파이썬에서는 비교 연산자를 연결하여 사용할 수 있습니다.
# 예를 들어, `1 < x < 10`은 `1 < x`와 `x < 10`이 모두 참인지 확인하는 것과 같습니다.

x = 5

# 예시 1: 연산자 체인 사용
# 1 < 5 이고 5 < 10 인가?
print(1 < x < 10)

# 예시 2: and 연산자로 명시적으로 표현
# 1 < x and x < 10 인가?
print(1 < x and x < 10)