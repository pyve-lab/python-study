# Python Operators
# 파이썬 연산자 (Python Operators)

# 연산자는 변수와 값에 대해 특정 작업을 수행하는 데 사용됩니다.
# 아래 예시에서는 + 연산자를 사용하여 두 값을 더합니다.

# 예시:
# print(10 + 5) 
# # 출력: 15

# + 연산자는 위 예시처럼 두 값을 더하는 데 자주 사용되지만,
# 변수와 값, 또는 두 변수를 더하는 데도 사용될 수 있습니다.

# 예시:
sum1 = 100 + 50      # 150 (100 + 50)
sum2 = sum1 + 250    # 400 (150 + 250)
sum3 = sum2 + sum2   # 800 (400 + 400)

# # 결과 확인을 위한 출력
# print(sum1)
# print(sum2)
# print(sum3)

