# Assignment Operators

# 할당 연산자는 값(value)을 변수(variable)에 할당(assign)하는 데 사용됩니다.
# 대부분의 할당 연산자는 산술 연산자와 결합하여 변수의 값을 연산한 후 다시 변수에 할당하는 단축 구문으로 사용됩니다.

# ------------------------------------------------------------------------------
# 할당 연산자 표 (Assignment Operators List)
# ------------------------------------------------------------------------------
#
# 연산자 | 예시 (Example) | 동일 표현 (Same As)

# =     | x = 5 | x = 5
# +=    | x += 3 | x = x + 3
# -=    | x -= 3 | x = x - 3
# *=    | x *= 3 | x = x * 3
# /=    | x /= 3 | x = x / 3
# %=    | x %= 3 | x = x % 3
# //=   | x //= 3 | x = x // 3
# **=   | x **= 3 | x = x ** 3
# &=    | x &= 3 | x = x & 3 (비트 연산: AND)
# |=    | x |= 3 | x = x | 3 (비트 연산: OR)
# ^=    | x ^= 3 | x = x ^ 3 (비트 연산: XOR)
# >>=   | x >>= 3 | x = x >> 3 (비트 연산: 오른쪽 시프트)
# <<=   | x <<= 3 | x = x << 3 (비트 연산: 왼쪽 시프트)
# :=    | print(x := 3) | x = 3; print(x) (바다코끼리 연산자, Python 3.8+)

# ------------------------------------------------------------------------------
# 예시: 할당 연산자 사용
# ------------------------------------------------------------------------------
x = 10
# x += 5 는 x = x + 5 와 같습니다.
x += 5 
print(x) # 출력: 15

y = 20
# y *= 2 는 y = y * 2 와 같습니다.
y *= 2
print(y) # 출력: 40


# ------------------------------------------------------------------------------
# 바다코끼리 연산자 (The Walrus Operator: :=)
# ------------------------------------------------------------------------------

# Python 3.8에서 도입된 := 연산자는 "바다코끼리 연산자"라고 알려져 있습니다.
# 이 연산자는 더 큰 표현식의 일부로 변수에 값을 할당합니다.
# 이를 통해 변수를 미리 정의할 필요 없이 조건문이나 루프 내에서 변수에 할당하고 바로 테스트할 수 있습니다.

numbers = [1, 2, 3, 4, 5]

# 예시 1: 바다코끼리 연산자 없이 (길이 계산과 조건 검사를 분리)
count = len(numbers)
if count > 3:
    print(f"List has {count} elements")

# 예시 2: 바다코끼리 연산자 사용 (할당과 조건 검사를 동시에)
# (count := len(numbers)) 부분에서 변수 count에 len(numbers) 값을 할당하고,
# 동시에 이 값이 > 3 조건을 만족하는지 확인합니다.
if (count := len(numbers)) > 3:
    print(f"List has {count} elements") # 출력: List has 5 elements