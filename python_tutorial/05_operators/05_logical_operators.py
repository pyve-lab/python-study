# Logical Operators

# 논리 연산자는 조건문(conditional statements)을 결합하는 데 사용됩니다.
# 논리 연산자의 결과는 항상 True 또는 False입니다.

# ------------------------------------------------------------------------------
# 논리 연산자 표 (Logical Operators List)
# ------------------------------------------------------------------------------
#
# 연산자 | 설명 (Description)                               | 예시 (Example)
# :---: | :---:                                            | :---:
# and   | 두 조건이 모두 참(True)이면 참(True)을 반환         | x < 5 and x < 10
# or    | 두 조건 중 하나라도 참(True)이면 참(True)을 반환     | x < 5 or x < 4
# not   | 결과를 반전시킵니다. 결과가 참이면 거짓(False) 반환 | not(x < 5 and x < 10)

# ------------------------------------------------------------------------------
# 예시 1: and 연산자 사용
# ------------------------------------------------------------------------------
# 숫자가 0보다 크고 10보다 작은지 테스트합니다.
x = 5

# 5 > 0 (True) AND 5 < 10 (True)
print(x > 0 and x < 10) 
# 출력: True

# ------------------------------------------------------------------------------
# 예시 2: or 연산자 사용
# ------------------------------------------------------------------------------
# 숫자가 5보다 작거나 10보다 큰지 테스트합니다.
x = 5

# 5 < 5 (False) OR 5 > 10 (False)
print(x < 5 or x > 10) 
# 출력: False

# ------------------------------------------------------------------------------
# 예시 3: not 연산자 사용 (결과 반전)
# ------------------------------------------------------------------------------
# 결과를 not 연산자로 반전시킵니다.
x = 5

# 내부 조건: (x > 3 and x < 10) -> (True and True) -> True
# not 연산 적용: not(True) -> False
print(not(x > 3 and x and x < 10)) 
# 출력: False