# Operator Precedence

# 연산자 우선순위는 연산이 수행되는 순서를 나타냅니다.

# ------------------------------------------------------------------------------
# 연산자 우선순위 표 (Precedence Order List)
# 가장 높은 우선순위부터 나열됩니다.
# ------------------------------------------------------------------------------
# Operator | Description
# :---: | :---
# () | Parentheses (괄호)
# ** | Exponentiation (거듭제곱)
# +x -x ~x | Unary plus, unary minus, and bitwise NOT (단항 플러스, 마이너스, 비트 NOT)
# * / // % | Multiplication, division, floor division, and modulus (곱셈, 나눗셈, 몫, 나머지)
# + - | Addition and subtraction (덧셈 및 뺄셈)
# << >> | Bitwise left and right shifts (비트 왼쪽 및 오른쪽 시프트)
# & | Bitwise AND (비트 AND)
# ^ | Bitwise XOR (비트 XOR)
# | | Bitwise OR (비트 OR)
# == != >= > <= < is is not in not in | Comparisons, identity, and membership operators (비교, 식별, 멤버십 연산자)
# not | Logical NOT (논리 NOT)
# and | AND (논리 AND)
# or | OR (논리 OR)

# ------------------------------------------------------------------------------
# 예시 1: 괄호 ()
# ------------------------------------------------------------------------------
# 괄호는 가장 높은 우선순위를 가지며, 괄호 안의 표현식이 먼저 평가되어야 합니다.
print((6 + 3) - (6 + 3))
# 출력 결과: 0 ( (9) - (9) )

# ------------------------------------------------------------------------------
# 예시 2: 곱셈 (*) 및 덧셈 (+)
# ------------------------------------------------------------------------------
# 곱셈 (*)은 덧셈 (+)보다 우선순위가 높으므로, 곱셈이 덧셈보다 먼저 평가됩니다.
# 5 * 3 (15)이 먼저 계산된 후, 100이 더해집니다.
print(100 + 5 * 3)
# 출력 결과: 115

# ------------------------------------------------------------------------------
# 왼쪽에서 오른쪽으로 평가 (Left-to-Right Evaluation)
# ------------------------------------------------------------------------------

# 두 연산자가 동일한 우선순위를 가질 경우, 표현식은 왼쪽에서 오른쪽으로 평가됩니다.

# 예시 3: 덧셈 (+)과 뺄셈 (-)
# 덧셈 (+)과 뺄셈 (-)은 동일한 우선순위를 가지므로, 왼쪽에서 오른쪽으로 평가됩니다.
# (5 + 4) -> 9
# (9 - 7) -> 2
# (2 + 3) -> 5
print(5 + 4 - 7 + 3)
# 출력 결과: 5