# 파이썬 vs 자바 연산자 비교 정리

# 파이썬과 Java는 기본 연산자(+, -, *, == 등)를 공유하지만,
# 파이썬은 특히 유용하거나 Java와 문법이 다른 연산자를 가집니다.

# ------------------------------------------------------------------------------
# 1. 산술 연산자 차이 (Arithmetic Operators)
# ------------------------------------------------------------------------------

# 파이썬은 나눗셈 처리와 거듭제곱에서 Java와 다릅니다.
x = 10
y = 3

# 1. 몫 나눗셈 (//)
# Python: 몫을 정수 형태로 반환하는 전용 연산자 (Java에는 없음)
print(f"1. 몫 나눗셈 (10 // 3): {x // y}")

# 2. 거듭제곱 (**)
# Python: 거듭제곱을 위한 전용 연산자 (Java는 Math.pow() 함수 사용)
print(f"2. 거듭제곱 (10 ** 3): {x ** y}")


# ------------------------------------------------------------------------------
# 2. 논리 연산자 문법 차이 (Logical Operators)
# ------------------------------------------------------------------------------

# 파이썬은 기호 대신 영어 단어를 사용합니다.
# (Java: &&, ||, ! 사용)

is_valid = True
is_admin = False

# Python: and, or, not 사용
print(f"논리 AND (True and False): {is_valid and is_admin}")


# ------------------------------------------------------------------------------
# 3. 식별 연산자 (Identity Operators) - Python 고유
# ------------------------------------------------------------------------------

# '값'이 아닌 '메모리 주소'가 같은지를 비교하는 연산자입니다.
# (Java는 메모리 참조 비교에 ==를 사용하며, is와 같은 명시적 연산자는 없습니다.)

a = [1, 2]
b = [1, 2]
c = a

# is: 두 변수가 같은 객체를 참조하는가?
print(f"3. is 연산자: a is c (같은 객체): {a is c}")
print(f"3. is 연산자: a is b (다른 객체): {a is b}")


# ------------------------------------------------------------------------------
# 4. 멤버십 연산자 (Membership Operators) - Python 고유
# ------------------------------------------------------------------------------

# 시퀀스(리스트, 문자열 등) 내부에 특정 요소가 포함되어 있는지 테스트합니다.
# (Java는 컬렉션의 .contains() 메서드를 사용해야 합니다.)

data_list = [10, 20, 30]

# in: 포함 여부
print(f"4. in 연산자: 10 in data_list: {10 in data_list}")

# not in: 미포함 여부
print(f"4. not in 연산자: 40 not in data_list: {40 not in data_list}")


# ------------------------------------------------------------------------------
# 5. 바다코끼리 연산자 (Walrus Operator, :=) - Python 3.8+ 고유
# ------------------------------------------------------------------------------

# 할당과 표현식을 동시에 처리하여 코드 길이를 줄일 수 있습니다. (Java에는 없음)

numbers = [1, 2]
# (count := len(numbers)) 에서 count 변수에 할당하고 조건문 평가
if (count := len(numbers)) > 1:
    print(f"5. 바다코끼리: {count}개 확인")