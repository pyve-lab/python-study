# Membership Operators
# 멤버십 연산자는 시퀀스(sequence)가 객체 안에 존재하는지 여부를 테스트하는 데 사용됩니다.
# 시퀀스에는 리스트, 튜플, 문자열 등이 포함됩니다.

# ------------------------------------------------------------------------------
# 멤버십 연산자 표 (Membership Operators List)
# ------------------------------------------------------------------------------
# 연산자 | 설명 (Description)                                     | 예시 (Example)

# in    | 지정된 값이 포함된 시퀀스가 객체 안에 있으면 True 반환    | x in y
# not in| 지정된 값이 포함된 시퀀스가 객체 안에 없으면 True 반환   | x not in y

# ------------------------------------------------------------------------------
# 예시 1: in 연산자 사용 (리스트)
# ------------------------------------------------------------------------------
# "banana"가 리스트에 있는지 확인합니다.
fruits = ["apple", "banana", "cherry"]

print("banana" in fruits) # True

# ------------------------------------------------------------------------------
# 예시 2: not in 연산자 사용 (리스트)
# ------------------------------------------------------------------------------
# "pineapple"이 리스트에 없는지 확인합니다.
fruits = ["apple", "banana", "cherry"]

print("pineapple" not in fruits) # True

# ------------------------------------------------------------------------------
# 문자열에서의 멤버십 (Membership in Strings)
# ------------------------------------------------------------------------------
# 멤버십 연산자는 문자열에서도 작동합니다.

# 예시:
text = "Hello World"

# "H"가 문자열에 있는지 확인 (대소문자 구분)
print("H" in text) # True

# "hello"가 문자열에 있는지 확인 (대소문자 구분)
print("hello" in text) # False

# "z"가 문자열에 없는지 확인
print("z" not in text) # True