# Comments
# 1. 파이썬 주석
# 파이썬에서 주석은 샾(#) 기호로 시작합니다.
# 주석은 코드에 대한 설명이나 메모를 작성할 때 사용되며, 파이썬 인터프리터는 주석을 무시합니다.
#This is a comment
print("Hello, World!")
# 주석은 줄의 끝에도 넣을 수 있으며, Python은 줄의 나머지 부분을 무시합니다.
print("파이썬 주석처리 줄 끝에 넣어보기")  # This comment is ignored by Python

# 2. 여러 줄 주석 처리할 때에는 #(샾)이 아닌 따옴표 3개를 사용할 수도 있습니다.
"""
이 예시는 여러 줄 주석을 보여줍니다.
파이썬은 이 부분을 무시합니다.
여러 줄에 걸쳐 설명을 작성할 때 유용합니다.
"""
print("여러 줄 주석 처리 예제입니다.")