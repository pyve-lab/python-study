
# =================================================================
# [1] 객체 지향 프로그래밍 (Object-Oriented Programming, OOP) 이란?
# =================================================================

# OOP는 "객체 지향 프로그래밍"의 약자입니다.

# 파이썬은 객체 지향 언어(Object-Oriented Language)입니다.
# 파이썬은 '클래스(Class)'와 '객체(Object)'를 사용하여 코드를 구조화합니다.
# 이를 통해 코드의 구성(Organization)을 개선하고 재사용성(Reusability)을 확보할 수 있습니다.


# -----------------------------------------------------------------
# [2] OOP의 주요 장점
# -----------------------------------------------------------------

# 1. 명확한 구조 제공
#    - 프로그램에 명확하고 체계적인 구조를 제공합니다.

# 2. 유지보수 및 디버깅 용이
#    - 코드의 유지 관리(Maintenance), 재사용(Reusability) 및 오류 수정(Debugging)을 더 쉽게 만듭니다.

# 3. DRY 원칙 준수 (Don't Repeat Yourself)
#    - 동일한 코드를 두 번 이상 작성하지 않도록 합니다.
#    - TIP: 반복되는 코드는 함수(Function)나 클래스(Class)로 옮겨 재사용하는 것이 좋습니다.

# 4. 재사용 가능한 애플리케이션 구축
#    - 더 적은 코드로 재사용 가능한 애플리케이션을 효율적으로 구축할 수 있습니다.


# -----------------------------------------------------------------
# [3] 클래스(Class)와 객체(Object)의 개념
# -----------------------------------------------------------------

# 클래스와 객체는 객체 지향 프로그래밍의 두 가지 핵심 구성 요소입니다.

# 1. 클래스 (Class)
#    - 객체의 "설계도" 또는 "틀"을 정의합니다.
#    - 객체가 가져야 할 속성(변수)과 동작(함수/메서드)을 정의합니다.

# 2. 객체 (Object) / 인스턴스 (Instance)
#    - 정의된 클래스를 기반으로 **실제로 만들어진 실체**입니다.
#    - 클래스라는 틀을 바탕으로 메모리에 할당된 고유한 데이터입니다.

# --- 예시 ---
# +-----------------+------------------------+
# |     클래스      |          객체          |
# +-----------------+------------------------+
# | Fruit (과일)    | Apple, Banana, Mango   |
# | Car (자동차)    | Volvo, Audi, Toyota    |
# +-----------------+------------------------+

# * 상속 (Inheritance)
# - 클래스에서 객체를 생성하는 순간(인스턴스화),
#   해당 객체는 클래스 내부에 정의된 모든 변수(속성)와 함수(메서드)를 상속(물려받아) 사용할 수 있게 됩니다.