
# =================================================================
# [1] 파이썬 클래스 및 객체 기본 개념
# =================================================================

# 1. 객체 지향 프로그래밍 (OOP)
# 파이썬은 객체 지향 프로그래밍 언어(OOPL)입니다.
# 파이썬에서는 거의 모든 것이 속성(properties)과 메서드(methods)를 가진 '객체(Object)'입니다.

# 2. 클래스란?
# 클래스는 객체 생성자(Constructor), 즉 객체를 만드는 "설계도(Blueprint)"와 같습니다.


# =================================================================
# [2] 클래스 생성 (Creating a Class)
# =================================================================

# 클래스를 정의할 때는 'class' 키워드를 사용합니다.

# 예: MyClass라는 이름의 클래스를 만들고, x라는 이름의 속성을 추가합니다.
class MyClass:
  # x는 클래스 속성(Class Attribute)
  x = 5
  


# =================================================================
# [3] 객체 생성 (Creating Objects) 및 사용
# =================================================================

# 정의된 클래스를 사용하여 객체(인스턴스)를 생성합니다.

print("--- [객체 생성 및 속성 접근] ---")
# 예: p1이라는 이름의 객체를 생성하고 x의 값을 출력합니다.
p1 = MyClass()
print(f"p1 객체의 속성 x 값: {p1.x}") 


# =================================================================
# [4] 여러 객체 생성 (Multiple Objects)
# =================================================================

# 하나의 클래스에서 여러 개의 독립적인 객체를 생성할 수 있습니다.

print("\n--- [여러 객체 생성 및 독립성] ---")
# 예: MyClass 클래스에서 p2, p3 두 개의 객체를 더 생성합니다.
p2 = MyClass()
p3 = MyClass()

print(f"p1.x: {p1.x}")
print(f"p2.x: {p2.x}")
print(f"p3.x: {p3.x}")

# 참고: 각 객체는 독립적이며 클래스 속성에 접근하거나 자체 복사본을 가집니다.


# =================================================================
# [5] 객체 삭제 (Deleting Objects)
# =================================================================

# 'del' 키워드를 사용하여 생성된 객체를 메모리에서 삭제할 수 있습니다.

print("\n--- [객체 삭제] ---")
print("p1 객체를 del p1 명령어로 삭제합니다.")

# 예: p1 객체를 삭제합니다.
del p1

# 참고: 다음 줄의 주석을 해제하고 실행하면 p1이 정의되지 않았다는 오류(NameError)가 발생합니다.
# print(p1.x)


# =================================================================
# [6] pass 문 (The pass Statement)
# =================================================================

# 'class' 정의는 비어있을 수 없습니다. 내용 없이 정의만 필요할 때 'pass'를 사용하여 오류를 방지합니다.

print("\n--- [pass 문 사용] ---")
# 예: 내용이 없는 Person 클래스 정의
class Person:
  pass
  
person_instance = Person()
print("pass 문을 사용하여 빈 Person 클래스의 객체를 생성했습니다.")