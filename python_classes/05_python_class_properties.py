
# =================================================================
# [1] 클래스 속성 기본 개념
# =================================================================

# 클래스 속성은 클래스 자체에 속하는 변수이며,
# 인스턴스(객체)가 생성될 때 해당 객체의 데이터로 사용되는 값들을 저장합니다.
# __init__ 안에서 정의되면 인스턴스 속성, 메서드 밖에서 정의되면 클래스 속성이 됩니다.



# =================================================================
# [2] 인스턴스 속성 정의 및 접근
# =================================================================

# 예:
# 인스턴스 속성을 가진 클래스 생성 및 접근

class Person:
  def __init__(self, name, age):
    self.name = name
    self.age = age

print("\n--- [인스턴스 속성 접근] ---")
p1 = Person("Emil", 36)

print(p1.name)
print(p1.age)



# =================================================================
# [3] 점 표기법을 이용한 속성 접근
# =================================================================

# 객체의 속성은 점(.) 표기법을 통해 접근할 수 있습니다.

# 예:
# 자동차 객체의 속성 접근

class Car:
  def __init__(self, brand, model):
    self.brand = brand
    self.model = model

print("\n--- [점 표기법으로 속성 접근] ---")
car1 = Car("Toyota", "Corolla")

print(car1.brand)
print(car1.model)



# =================================================================
# [4] 속성 값 수정
# =================================================================

# 객체가 생성된 이후에도 속성 값을 자유롭게 변경할 수 있습니다.

# 예:
# 나이 속성 변경

class Person:
  def __init__(self, name, age):
    self.name = name
    self.age = age

print("\n--- [속성 값 수정] ---")
p1 = Person("Tobias", 25)
print(p1.age)

p1.age = 26
print(p1.age)



# =================================================================
# [5] 속성 삭제
# =================================================================

# del 키워드를 이용하면 특정 객체의 속성을 삭제할 수 있습니다.

# 예:
# age 속성을 삭제

class Person:
  def __init__(self, name, age):
    self.name = name
    self.age = age

print("\n--- [속성 삭제] ---")
p1 = Person("Linus", 30)

del p1.age

print(p1.name)   # 정상 동작
# print(p1.age)  # 삭제되었기 때문에 오류 발생



# =================================================================
# [6] 클래스 속성과 인스턴스 속성 비교
# =================================================================

# __init__ 내부에서 정의된 속성 → 인스턴스 속성(객체마다 독립적)
# 메서드 외부에서 정의된 속성 → 클래스 속성(모든 객체가 공유)

# 예:
# 클래스 속성과 인스턴스 속성 비교

class Person:
  species = "Human"  # 클래스 속성

  def __init__(self, name):
    self.name = name # 인스턴스 속성

print("\n--- [클래스 속성 vs 인스턴스 속성] ---")
p1 = Person("Emil")
p2 = Person("Tobias")

print(p1.name)
print(p2.name)
print(p1.species)
print(p2.species)



# =================================================================
# [7] 클래스 속성 수정
# =================================================================

# 클래스 속성을 수정하면 해당 클래스로부터 생성된 모든 객체에 영향을 미칩니다.

# 예:
# 클래스 속성 값 변경

class Person:
  lastname = ""

  def __init__(self, name):
    self.name = name

print("\n--- [클래스 속성 수정] ---")
p1 = Person("Linus")
p2 = Person("Emil")

Person.lastname = "Refsnes"

print(p1.lastname)
print(p2.lastname)



# =================================================================
# [8] 객체에 새 속성 동적 추가
# =================================================================

# 객체 생성 이후에도 새로운 속성을 직접 추가할 수 있으며,
# 이렇게 추가된 속성은 해당 객체에만 존재합니다.

# 예:
# 인스턴스에 새로운 속성 age, city 추가

class Person:
  def __init__(self, name):
    self.name = name

print("\n--- [객체에 새 속성 동적 추가] ---")
p1 = Person("Tobias")

p1.age = 25
p1.city = "Oslo"

print(p1.name)
print(p1.age)
print(p1.city)

# 참고:
# 이렇게 추가한 속성은 p1 객체에만 존재하며,
# Person 클래스의 다른 객체에는 자동으로 추가되지 않습니다.
