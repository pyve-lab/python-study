# 비교 연산자는 두 값을 비교하는 데 사용됩니다.

# Operator	Name	Example
# ==	Equal	x == y
x = 5
y = 3
print(x == y)
# False

# !=	Not equal	x != y
x = 5
y = 3
print(x != y)
# True

# >	Greater than	x > y
x = 5
y = 3
print(x > y)
# True

# <	Less than	x < y
x = 5
y = 3
print(x < y)
# False

# >=	Greater than or equal to	x >= y
x = 5
y = 3
print(x >= y)
# True

# <=	Less than or equal to	x <= y
x = 5
y = 3
print(x <= y)
# False

