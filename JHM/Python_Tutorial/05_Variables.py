#######################################
# 대소문자 구분
#######################################
# 변수 이름은 대소문자를 구분합니다.

# 예
# 이렇게 하면 두 개의 변수가 생성됩니다.
a = 4
A = "Sally" # #A will not overwrite a
print(a); print(A);
# 4
# Sally

